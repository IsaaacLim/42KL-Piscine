#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char alpha;

	alpha = 0;
	while (str[alpha] != '\0')
	{
		ft_putchar(str[alpha]);
		alpha++;
	}
}
